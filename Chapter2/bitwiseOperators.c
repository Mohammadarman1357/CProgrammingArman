#include <stdio.h>

int main()
{
    int a = 5;
    int b = 6;

    printf("a & b is : %d \n", a & b);
    printf("a | b is : %d \n", a | b);
    printf("a ^ b is : %d \n", a ^ b);
    printf("~ a is : %d \n", ~a);
    printf("a << 2 is : %d \n", a << 2);
    printf("a >> 2 is : %d \n", a >> 2);

    return 0;
}
